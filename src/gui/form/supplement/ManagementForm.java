 private void save_constraint()
 {
   try
     {
    if (this.constraint==null)
        this.constraint=new Constraints();
     //
     if(this.Constraint_TotalDuration_jTextField.getText()!=null
     && !this.Constraint_TotalDuration_jTextField.getText().equals(""))
             this.constraint.setTotalDuration(Double.parseDouble(this.Constraint_TotalDuration_jTextField.getText()));
     //
     if(this.Constraint_ElapsedTime_jTextField.getText()!=null
     && !this.Constraint_ElapsedTime_jTextField.getText().equals(""))
             this.constraint.setElapsedTime(Double.parseDouble(this.Constraint_ElapsedTime_jTextField.getText()));
    //
     if(this.Constraint_TotalFunds_jTextField.getText()!=null
     && !this.Constraint_TotalFunds_jTextField.getText().equals(""))
        this.constraint.setTotalFunds(Double.parseDouble(this.Constraint_TotalFunds_jTextField.getText()));

     if(this.Constraint_UsedFunds_jTextField.getText()!=null
     && !this.Constraint_UsedFunds_jTextField.getText().equals(""))
        this.constraint.setUsedFunds(Double.parseDouble(this.Constraint_UsedFunds_jTextField.getText()));

      Global.project.getSupplements().getManagement().setConstraint(constraint);

      Global.project.getSupplements().getManagement().store(null);

     } catch (Exception ex) {
            Logger.getLogger(ManagementForm.class.getName()).log(Level.SEVERE, null, ex);
     }
  }

