     if (this.resource!=null && this.resource.getRequirements()==null)
         this.resource.setRequirements(new Requirements());
     //

     if (this.resource!=null && this.resource.getRequirements()!=null &&  requirement!=null)
            this.resource.getRequirements().save(requirement);
     
