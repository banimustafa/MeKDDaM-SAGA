//****************************************************************************
//                              Delete Resource
//DDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDD
//****************************************************************************
 private void delete_resource()
    {
    int index= Global.project.getSupplements().getManagement().getResources().getResourcesList().indexOf(this.resource);
    if (this.resource!=null && Global.project.getSupplements().getManagement().getResources()!=null)
        if (Global.project.getSupplements().getManagement().getResources().delete(this.resource)==0);
        {
            if (Global.project.getSupplements().getManagement().getResources().getResourcesList().size()>0)
            {
                if (index==0)
                    this.resource = Global.project.getSupplements().getManagement().getResources().getResource(index);
                else
                    this.resource = Global.project.getSupplements().getManagement().getResources().getResource(index-1);
                this.refresh_requirement();
                this.populate_resource();
            }
            else
            this.clear_resource();
        }

     this.save_resource();
     
 }



