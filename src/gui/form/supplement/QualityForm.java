//****************************************************************************
//                              Delete Resource
//DDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDD
//****************************************************************************
 private void delete_quality()
    {
    int index= Global.project.getSupplements().getQuality().getSpecificationsList().indexOf(this.qualitySpecification);
    if (this.qualitySpecification!=null && Global.project.getSupplements().getQuality()!=null)
        if (Global.project.getSupplements().getQuality().delete(this.qualitySpecification)==0);
        {
            if (Global.project.getSupplements().getQuality().getSpecificationsList().size()>0)
            {
                if (index==0)
                    this.qualitySpecification = Global.project.getSupplements().getQuality().get(index);
                else
                    this.qualitySpecification = Global.project.getSupplements().getQuality().get(index-1);

                this.populate_quality();
            }
            else
            this.clear_quality();
        }

     this.save_quality();
 }



