 private void populate_quality()
{
     this.clear_qualityForm();
     
         if (this.qualitySpecification!=null && qualitySpecification.getSpecificationURL()!=null)
            this.URL_jTextField.setText(qualitySpecification.getSpecificationURL().toString());
         else
           this.URL_jTextField.setText("");
         //--------------------------------------------------------------------
         if (this.qualitySpecification!=null && qualitySpecification.getDescription()!=null)
            this.Description_jTextArea.setText(qualitySpecification.getDescription());
         else
           this.Description_jTextArea.setText("");
         //--------------------------------------------------------------------
         if (this.qualitySpecification!=null && qualitySpecification.getRelease()!=null)
            this.Release_jTextField.setText(qualitySpecification.getRelease());
         else
           this.Release_jTextField.setText("");

         //--------------------------------------------------------------------
         if (this.qualitySpecification!=null && qualitySpecification.getDomainType()!=null)
            this.Domain_jComboBox.setSelectedIndex(qualitySpecification.getDomainType().ordinal()+1);
         else
             this.Domain_jComboBox.setSelectedIndex(0);

        if (this.qualitySpecification!=null && qualitySpecification.getQualityType()!=null)
            this.Type_jComboBox.setSelectedIndex(qualitySpecification.getQualityType().ordinal()+1);
        else
            this.Type_jComboBox.setSelectedIndex(0);


     this.populateForm();
 }







