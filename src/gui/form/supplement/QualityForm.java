 private void refresh_quality()
 {
    if (Global.project!=null &&
        Global.project.getSupplements().getQuality()!=null&&
        Global.project.getSupplements().getQuality()!=null &&
        Global.project.getSupplements().getQuality().getSpecificationsList().size()>0
        )
    {
        this.qualitySpecification = Global.project.getSupplements().getQuality().getFirst();
        this.populate_quality();
    }
    //else
        //System.out.print(" No Resource is available");
}


