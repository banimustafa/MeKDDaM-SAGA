 private void save_quality()
 {
 try
   {
    if (this.qualitySpecification==null)
        this.qualitySpecification=new QualitySpecification();
     //
     try{
        if(this.URL_jTextField!=null && !this.URL_jTextField.getText().equals(""))
            this.qualitySpecification.setSpecificationURL(new URL(this.URL_jTextField.getText()));
        } catch (MalformedURLException x) {
              Logger.getLogger(QualityForm.class.getName()).log(Level.SEVERE, null, x);
         }

     if(this.Description_jTextArea!=null &&
       !this.Description_jTextArea.getText().equals(""))
        {
           this.qualitySpecification.setDescription(this.Description_jTextArea.getText());
        }

    if(this.Release_jTextField!=null && !this.Release_jTextField.getText().equals(""))
        this.qualitySpecification.setRelease(this.Release_jTextField.getText());

    //
     this.qualitySpecification.setDomainType(this.Domain_jComboBox.getSelectedIndex());
     this.qualitySpecification.setQualityType(this.Type_jComboBox.getSelectedIndex());
    //

     Global.project.getSupplements().getQuality().save(qualitySpecification);
     Global.project.getSupplements().getQuality().store(null);
         
   } catch (Exception ex) {
     Logger.getLogger(QualityForm.class.getName()).log(Level.SEVERE, null, ex);
  }
}



