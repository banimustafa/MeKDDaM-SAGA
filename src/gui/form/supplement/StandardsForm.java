 private void save_standard()
 {
   try
     {
    if (this.standard==null)
        this.standard=new Standard();
    
   try{
       if(this.URL_jTextField!=null && !this.URL_jTextField.getText().equals(""))
       this.standard.setSpecificationURL(new URL(this.URL_jTextField.getText()));
       } catch (MalformedURLException x) {
         Logger.getLogger(StandardsForm.class.getName()).log(Level.SEVERE, null, x);
       }

     if(this.Description_jTextArea!=null
     && !this.Description_jTextArea.getText().equals(""))
        this.standard.setDescription(this.Description_jTextArea.getText());
    
    if(this.Release_jTextField!=null && !this.Release_jTextField.getText().equals(""))        
        this.standard.setRelease(this.Release_jTextField.getText());

    this.standard.setType(this.Type_jComboBox.getSelectedIndex());

    if (Global.project.getSupplements()==null)
        Global.project.setSupplements(new Supplements());

    if (Global.project.getSupplements().getStandards()==null)
        Global.project.getSupplements().setStandards(new Standards());
            
     if (Global.project.getSupplements()!=null && Global.project.getSupplements().getStandards()!=null && this.standard!=null)
            Global.project.getSupplements().getStandards().save(this.standard);
    
      Global.project.getSupplements().getStandards().store(null);

     } catch (Exception ex) {
            Logger.getLogger(StandardsForm.class.getName()).log(Level.SEVERE, null, ex);
     }
  }



