 private void delete_standard()
    {
    int index= Global.project.getSupplements().getStandards().getStandardsList().indexOf(this.standard);
    if (this.standard!=null && Global.project.getSupplements().getStandards()!=null)
        if (Global.project.getSupplements().getStandards().delete(this.standard)==0);
        {
            if (Global.project.getSupplements().getStandards().getStandardsList().size()>0)
            {
                if (index==0)
                    this.standard = Global.project.getSupplements().getStandards().get(index);
                else
                    this.standard = Global.project.getSupplements().getStandards().get(index-1);

                this.populate_standard();
            }
            else
            this.clear_standard();
        }

     this.save_standard();
 }



